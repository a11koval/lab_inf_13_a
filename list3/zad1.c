

int main(void) {
    int i;

    // od 1 do 10
    for (i = 1; i <= 10; i++)
        printf("%d ", i);
    printf("\n");

    // od 10 do 70
    for (i = 10; i <= 70; i++)
        printf("%d ", i);
    printf("\n");

    // od -15 do 5
    for (i = -15; i <= 5; i++)
        printf("%d ", i);
    printf("\n");

    return 0;
}
